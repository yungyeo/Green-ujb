package Q9025;

public class Main {
	public static void main(String[] args) {
		int a = 0, b = 1, c = 2;

		if (a == 1 && b == 0) {
			System.out.print("1 ");
		} else {
			System.out.print("0 ");
		}

		if (a == 0 || b == 1) {
			System.out.print("1 ");
		} else {
			System.out.print("0 ");
		}

		if (b == 1 && c == 2) {
			System.out.print("1 ");
		} else {
			System.out.print("0 ");
		}

		if (a == 0) {
			System.out.print("1 ");
		} else {
			System.out.print("0 ");
		}
	}
}
